package org.example;

public class Calculator {
    public int plus(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public double division(double a, double b) {
        return a / b;
    }

    public int multiplication(int a, int b) {
        return a * b;
    }
}
